type FixLengthTuple<
  N extends number,
  A extends any[] = [],
> = A["length"] extends N ? A : FixLengthTuple<N, [...A, any]>

type Chunk<T extends any[], U extends number> = T extends [
  ...FixLengthTuple<U>,
  ...infer Rest,
] // 这样子 Rest 就空出了前面 U 个 Element
  ? T extends [...infer R, ...Rest] // R 就是前 U 个 Element 组成的 Array
    ? [R, ...Chunk<Rest, U>]
    : []
  : T extends []
  ? []
  : [T]

